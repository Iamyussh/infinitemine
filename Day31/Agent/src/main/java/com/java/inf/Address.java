package com.java.inf;

public class Address {
		
	private	int addressid;
	private String addressline;
	private String city;
	private String state;
	private String country;
	private int pincode;
	private int getAddressid() {
		return addressid;
	}
	private void setAddressid(int addressid) {
		this.addressid = addressid;
	}
	private String getAddressline() {
		return addressline;
	}
	private void setAddressline(String addressline) {
		this.addressline = addressline;
	}
	private String getCity() {
		return city;
	}
	private void setCity(String city) {
		this.city = city;
	}
	private String getState() {
		return state;
	}
	private void setState(String state) {
		this.state = state;
	}
	private String getCountry() {
		return country;
	}
	private void setCountry(String country) {
		this.country = country;
	}
	private int getPincode() {
		return pincode;
	}
	private void setPincode(int pincode) {
		this.pincode = pincode;
	}
	
	
	
	
	
	
}
